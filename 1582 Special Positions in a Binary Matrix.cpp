int numSpecial(vector<vector<int>>& mat) {
        int m = mat.size();
        int n = mat[0].size(), special = 0;
        vector<pair<int,int>> point;
        for(int i = 0; i<m ;++i)
            for(int j=0;j<n;j++)
                if(mat[i][j]==1)
                    point.push_back({i,j});
        
        for(auto p: point){
            int x = p.first;
            int y = p.second;
            int cnt = 0;
            for(int i=0;i<n;++i)
                if(mat[x][i]==1)
                    ++cnt;
            for(int i=0;i<m;++i)
                if(mat[i][y]==1)
                    ++cnt;
            if(cnt==2)
                ++special;
        }
        return special;
        
    }

 int numSpecial1(vector<vector<int>>& mat) {
        int m = mat.size();
        int n = mat[0].size();
        
        vector<int> rowSum(m, 0);
        vector<int> colSum(n, 0);
        
        // Count row and column sums
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                if (mat[i][j] == 1) {
                    rowSum[i]++;
                    colSum[j]++;
                }
            }
        }
        
        int count = 0;
        
        // Check special positions
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                if (mat[i][j] == 1 && rowSum[i] == 1 && colSum[j] == 1) {
                    count++;
                }
            }
        }
        
        return count;
    }

int numSpecial2(vector<vector<int>>& mat) {
    int m = mat.size(), n = mat[0].size();
    vector<int> row(m,0), col(n,0);

    for(int i=0;i<m;i++)
        for(int j=0;j<n;j++)
            if(mat[i][j]==1){
                row[i]++;
                col[j]++;
            }

    int count = 0;
    for(int i=0;i<m;i++)
        for(int j=0;j<n;j++)
            if(mat[i][j]==1 && row[i]==1 && col[j]==1)
                count++;

    return count;
}

